package com.XML_Parsing;

public class Grammar {
	
	protected String Value;
	protected String RootLanguage;
	protected String Mode;
	protected String Builtin;
	protected String BuiltinMode;
	protected String Type;
	protected String Options;
	
	public String getValue() {
		return Value;
	}
	public void setValue(String value) {
		Value = value;
	}
	public String getRootLanguage() {
		return RootLanguage;
	}
	public void setRootLanguage(String rootLanguage) {
		RootLanguage = rootLanguage;
	}
	public String getMode() {
		return Mode;
	}
	public void setMode(String mode) {
		Mode = mode;
	}
	public String getBuiltin() {
		return Builtin;
	}
	public void setBuiltin(String builtin) {
		Builtin = builtin;
	}
	public String getBuiltinMode() {
		return BuiltinMode;
	}
	public void setBuiltinMode(String builtinMode) {
		BuiltinMode = builtinMode;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getOptions() {
		return Options;
	}
	public void setOptions(String options) {
		Options = options;
	}
	
}
