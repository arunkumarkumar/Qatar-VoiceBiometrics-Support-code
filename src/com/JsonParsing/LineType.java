package com.JsonParsing;

public class LineType {
	
	protected String Name;
	protected String PrimaryLocation;
	protected String PrimaryTimeout;
	protected String SecondaryLocation;
	protected String SecondaryTimeout;
	protected String TernaryLocation;
	protected String TernaryTimeout;
	protected String QaternaryLocation;
	protected String IsActive;
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPrimaryLocation() {
		return PrimaryLocation;
	}
	public void setPrimaryLocation(String primaryLocation) {
		PrimaryLocation = primaryLocation;
	}
	public String getPrimaryTimeout() {
		return PrimaryTimeout;
	}
	public void setPrimaryTimeout(String primaryTimeout) {
		PrimaryTimeout = primaryTimeout;
	}
	public String getSecondaryLocation() {
		return SecondaryLocation;
	}
	public void setSecondaryLocation(String secondaryLocation) {
		SecondaryLocation = secondaryLocation;
	}
	public String getSecondaryTimeout() {
		return SecondaryTimeout;
	}
	public void setSecondaryTimeout(String secondaryTimeout) {
		SecondaryTimeout = secondaryTimeout;
	}
	public String getTernaryLocation() {
		return TernaryLocation;
	}
	public void setTernaryLocation(String ternaryLocation) {
		TernaryLocation = ternaryLocation;
	}
	public String getTernaryTimeout() {
		return TernaryTimeout;
	}
	public void setTernaryTimeout(String ternaryTimeout) {
		TernaryTimeout = ternaryTimeout;
	}
	public String getQaternaryLocation() {
		return QaternaryLocation;
	}
	public void setQaternaryLocation(String qaternaryLocation) {
		QaternaryLocation = qaternaryLocation;
	}
	public String getIsActive() {
		return IsActive;
	}
	public void setIsActive(String isActive) {
		IsActive = isActive;
	}

}
